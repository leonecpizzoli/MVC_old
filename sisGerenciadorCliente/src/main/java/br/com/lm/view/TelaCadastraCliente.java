    private void jbuOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuOKActionPerformed

        ctrlCli = new ControladorCliente();
        try {
            preencherObjetos();
            if (cliente.confirmaSenha() == false){
                    Mensagem.showMsg("As senhas não batem");
            }
                    if (cliente.validaCliente() == false) {
                Mensagem.showMsg("Impossível de salvar!");
            }else{
                ctrlCli.incluir(cliente);
                Mensagem.showMsg("Salvo!");
                }
        }catch (NullPointerException ex) {
            Mensagem.showMsg("Algum campo está vazio!\n");
        }
       

    }//GEN-LAST:event_jbuOKActionPerformed

