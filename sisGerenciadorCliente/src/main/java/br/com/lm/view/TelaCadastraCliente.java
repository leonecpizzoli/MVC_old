    private void preencherObjetos() {
        // Inicializar Cliente
        cliente = new Cliente();

        cliente.setNome(jtfNome.getText());
        cliente.setLogin(jtfLogin.getText());
        cliente.setSenha(jpfSenha.getText());
        cliente.setConfirma(jpfConfirm.getText());
    }
